# O comando in range() repete uma variável o tanto de vezes que for especificado.

for i in range(5):
    print(i)

# O comando "for i in palavra:" faz a variável "i" repetir a quantidade de
# caracteres que contém a variável "palavra".

palavra = 'codigofonte'
for i in palavra:
    print(i)

# Listas

lista = [1, 2, 54, 'oi']
for num in lista:
    print(num)
